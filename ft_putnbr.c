#include <printf.h>

int	ft_putnbr(int nb)
{
	int	mod;

	if (nb == -2147483648)
		ft_putchar(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			ft_putchar(1, "-", 1);
			nb = -nb;
		}
		if (nb >= 10)
		{
			ft_putnbr(nb / 10);
		}
		mod = (nb % 10 + '0');
		ft_putchar(1, &mod, 1);
	}
}
